    private void search_lblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_search_lblMouseClicked
        // TODO add your handling code here:
         String url = "JDBC:sqlite:temp.db";
        Connection conn = null; 
        
        try{
            Class.forName("org.sqlite.JDBC");
            conn = DriverManager.getConnection(url);
            
        }catch (ClassNotFoundException | SQLException ex){
            ex.getStackTrace();
        }
        
        String query = "SELECT *from category;";
        try{
            PreparedStatement stmt = conn.prepareStatement(query);
                
                ResultSet res =stmt.executeQuery();
                
                while(res.next()){
                    String id = String.valueOf(res.getInt("category_id"));
                    
                    String CategoryName = res.getString("category_name");
                    
                    
                    String data[] = {id,CategoryName};
                    DefaultTableModel TblModel = (DefaultTableModel)category_tbl.getModel();
                    
                    
                    TblModel.addRow(data);
                }
                res.close();
                conn.close();
                
        }catch(SQLException ex){
            ex.getStackTrace();
        }
    }//GEN-LAST:event_search_lblMouseClicked

