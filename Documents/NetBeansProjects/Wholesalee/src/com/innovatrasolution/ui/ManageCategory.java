    private void add_categorybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_categorybtnActionPerformed
        // TODO add your handling code here:
        String url = "JDBC:sqlite:temp.db";
        Connection conn = null; 
        
        try{
            Class.forName("org.sqlite.JDBC");
            conn = DriverManager.getConnection(url);
            
        }catch (ClassNotFoundException | SQLException ex){
            ex.getStackTrace();
        }
        
         String CategoryName = cat_name_txt1.getText();
         String ImgUrl = img_linklbl.getText();
         System.out.println(ImgUrl);
         
         
        if( CheckField(CategoryName) == true ){
            String query = "INSERT into category(category_name, category_image) VALUES(?,?);";
            try{
                
                PreparedStatement stmt = conn.prepareStatement(query);
                stmt.setString(1, CategoryName);
                stmt.setString(2,ImgUrl);
                stmt.executeUpdate();
                CategoryAddedDialog dialog = new CategoryAddedDialog();
                dialog.setVisible(true);
                this.setVisible(false);
            } catch(SQLException ex){
                ex.getStackTrace();
            }
        }
        else if(CheckField(CategoryName)== false ){
            //Else incase of Category Textfield is null 
            warning_lbl.setVisible(true);
        }
    }//GEN-LAST:event_add_categorybtnActionPerformed

